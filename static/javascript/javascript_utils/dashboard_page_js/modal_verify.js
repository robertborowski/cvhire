// Get modal element
var modalDetails = document.getElementById('quiz-rules-details-modal');
// Get open modal button
var quizRulesActivateModal = document.getElementById('quiz-rules-activate-modal');
// Get open modal button
var closeModalXButton = document.getElementsByClassName('close-modal-x-button')[0];

// Listen for open click
quizRulesActivateModal.addEventListener('click', openModal);
// Listen for open click
closeModalXButton.addEventListener('click', closeModal);
// Listen for outside click
window.addEventListener('click', outsideClick);

// Function to open modal
function openModal(){
  modalDetails.style.display = 'block';
}
// Function to close modal
function closeModal(){
  modalDetails.style.display = 'none';
}
// Function to close modal if outside click
function outsideClick(e){
  if(e.target == modalDetails){
    modalDetails.style.display = 'none';
  }
}